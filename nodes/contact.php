		<h1>Getting to the Hall</h1>
		<p>The College has no parking facilities for visitors, so we strongly recommend that you travel by public transport wherever possible.</p>
		
		<h2>By coach/bus</h2>
		<p>Both the major bus companies in Oxford, the Oxford Tube and Oxford Express, run frequent coach services from London Victoria. Ask the driver to drop you at the Queen's Lane/High Street bus stop, which is opposite the College.</p>
		<p>For visitors from overseas, the Airline bus service offers frequent coach services from London Heathrow and London Gatwick airports. These also stop at Queen's Lane, right by the College.</p>
		<p>Oxford is well served by National Express coach services from all over the country.</p>
		
		<h2>By train</h2>
		<p>Direct rail services connect Oxford to London, Birmingham, the South Coast and Northern England, and connections are available to many other parts of the country. National Rail Enquiries can help you plan your journey. Trains from London leave at least hourly and the journey takes approximately 60 minutes.</p>
		<p>If arriving by train, the College is about 15-20 minutes walk from the station, or the number 5 bus from the station will drop you at Queen's Lane, right outside the College.</p>
		
		<h2>Park & Ride</h2>
		<p>If travelling by car we strongly recommend you use one of Oxford's Park & Ride services, which run into the city centre from five car parks around the edge of the city.</p>
		
		<h2>By car</h2>
		<p>If you do decide to drive into the city centre parking can be difficult to find and is expensive. The nearest public car park to the College is in St Clement's Street, or there is very limited on-street parking around the city centre. Do check the signs carefully to ensure you follow any parking regulations, as Oxford traffic wardens are very active. There is a disabled parking space available directly outside the main entrance to the College.</p>
